"""
Thesis results aggregator — combines outputs from all Tier-1 scripts into
LaTeX-ready tables and matplotlib plots for the thesis manuscript.

What this script does
─────────────────────
1. Locates the most recent quantization_sweep_*, agentic_ablation_*, and
   latency_memory_* result directories under ./evaluation_results/.
2. Reads each summary.json + per-query JSONL.
3. Emits to a single output directory:
       table_quantization.tex      — \\begin{table}...\\end{table} LaTeX.
       table_ablation.tex          — same, with marginal-Δ columns.
       table_latency.tex           — per-stage timing + budget compliance.
       figure_pareto.png           — latency vs. EM (one point per model).
       figure_ablation_waterfall.png — EM gain per added component.
       figure_stage_breakdown.png  — stacked bar of S_P / S_N / S_V time.
       README_HOW_TO_USE.md        — paste-into-thesis instructions.

Why this exists
───────────────
Hand-formatting LaTeX tables from CSVs is error-prone and the chapter-to-
script mapping gets lost three months later when you defend. This script is
the single source of truth: "rerun this, paste the .tex files, done."

Usage:
    python -X utf8 -m src.thesis_evaluations.thesis_results_aggregator \\
        --output ./evaluation_results/thesis_final

Optional:
    --quantization-dir <path>   pin a specific quantization run (else latest)
    --ablation-dir <path>       pin a specific ablation run
    --latency-dir <path>        pin a specific latency run

Dependencies (lazy-imported; aggregator runs without them but skips plots):
    matplotlib              for the figures
    pandas                  optional, only for advanced cross-tab views

Thesis mapping
──────────────
This is the ONE script you run last, after the three Tier-1 scripts have
produced their output directories. The resulting .tex files go directly
into the thesis source; the .png plots into the figures/ directory.
"""

