THESIS_CLAIMS = [
    ("Quantized Small Language Models",
     "quantization_sweep — multi-model EM/latency/RAM table"),
    ("Agentic Verification (contribution)",
     "agentic_ablation — 5-row marginal-Δ table"),
    ("Resource-Constrained Devices",
     "latency_memory_profile — per-stage timing, peak RSS, budget compliance"),
    ("Hybrid Retrieval-Augmented Generation",
     "benchmark_datasets ablation — vector/graph/hybrid SF-F1 comparison"),
    ("Reasoning Fidelity",
     "EM, F1, and EM|retr.ok across all of the above"),
]


def validate_coverage(qsweep: Optional[Dict],
                       ablation: Optional[Dict],
                       latency: Optional[Dict]) -> List[Tuple[str, str, bool]]:
    """For each thesis claim, return (claim, supporting_script, present)."""
    out: List[Tuple[str, str, bool]] = []
    out.append(("Quantized Small Language Models",
                "quantization_sweep",
                qsweep is not None and bool(qsweep.get("rows"))))
    out.append(("Agentic Verification (contribution)",
                "agentic_ablation",
                ablation is not None and bool(ablation.get("rows"))))
    out.append(("Resource-Constrained Devices",
                "latency_memory_profile",
                latency is not None and bool(latency.get("total_ms"))))
    out.append(("Hybrid Retrieval-Augmented Generation",
                "benchmark_datasets ablation (SF-F1 column)",
                qsweep is not None and bool(qsweep.get("rows"))))
    out.append(("Reasoning Fidelity",
                "EM/F1/EM|retr.ok across all scripts",
                qsweep is not None and ablation is not None))
    return out


